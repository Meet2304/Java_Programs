import java.util.*;
public class ArithmaticOp {
    public static void main(String[] args){
        int a=5;
        int b=10;
        System.out.println("The sum of 5 and 10 is:" + (a+b));
        System.out.println("The difference of 5 and 10 is:" + (a-b));
        System.out.println("The product of 5 and 10 is:" + (a*b));
        System.out.println("The quotient of 5 and 10 is:" + (a+b));
    }
}
