import java.util.*;
public class ScientificCalc {
    public static void main(String[] args){
        System.out.println("The absolute value of -5 is:" + Math.abs(-5));
        System.out.println("The minimum value from 1 and 2 is:" + Math.min(1,2));
        System.out.println("The maximum value from 1 and 2 is:" + Math.max(1,2));
        System.out.println("The sine of 30deg is:" + Math.sin(30));
        System.out.println("The cosine of 60deg is:" + Math.cos(60));
        System.out.println("The tan of 45deg is:" + Math.tan(45));
        System.out.println("The natural logarithm of 22 is:" + Math.log(22));
        System.out.println("The logarithm of 22 with base 10 is:" + Math.log10(22));
        System.out.println("15.49 rounded to nearest integer is: " + Math.round(15.49));   
    }

}